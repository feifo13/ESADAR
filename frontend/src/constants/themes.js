function createLabTheme(index, mode, config) {
  const padded = String(index + 1).padStart(2, '0');

  if (mode === 'light') {
    return {
      id: `sharp-lab-light-${padded}`,
      label: `Sharp Lab Light ${padded}`,
      section: 'Sharp Lab',
      swatch: [config.bg, config.aqua, config.orange],
      vars: {
        '--bg': config.bg,
        '--surface': config.surface,
        '--surface-soft': config.soft,
        '--text': config.text,
        '--muted': config.muted,
        '--border': config.border,
        '--aqua': config.aqua,
        '--orange': config.orange,
        '--navy': config.navy,
      },
    };
  }

  return {
    id: `sharp-lab-dark-${padded}`,
    label: `Sharp Lab Dark ${padded}`,
    section: 'Sharp Lab',
    swatch: [config.bg, config.aqua, config.orange],
    vars: {
      '--bg': config.bg,
      '--surface': config.surface,
      '--surface-soft': config.soft,
      '--text': config.text,
      '--muted': config.muted,
      '--border': config.border,
      '--aqua': config.aqua,
      '--orange': config.orange,
      '--navy': config.navy,
    },
  };
}

