export const THEME_IDS = THEME_OPTIONS.map((theme) => theme.id);
export const THEME_MAP = Object.fromEntries(THEME_OPTIONS.map((theme) => [theme.id, theme]));

export function getThemeGroups() {
  return THEME_OPTIONS.reduce((acc, theme) => {
    const currentGroup = acc.find((group) => group.section === theme.section);
    if (currentGroup) {
      currentGroup.items.push(theme);
      return acc;
    }
    acc.push({ section: theme.section, items: [theme] });
    return acc;
  }, []);
}
